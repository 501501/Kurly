package e.market.kurly.mypage.destination;

public class DestinationDTO {
	private String id;
	private String address_loca;
	private String address_post;
	private String name;
	private String phone_num;
	private String shipping_type;
	private String default_type;
	private String check_type;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAddress_loca() {
		return address_loca;
	}
	public void setAddress_loca(String address_loca) {
		this.address_loca = address_loca;
	}
	public String getAddress_post() {
		return address_post;
	}
	public void setAddress_post(String address_post) {
		this.address_post = address_post;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone_num() {
		return phone_num;
	}
	public void setPhone_num(String phone_num) {
		this.phone_num = phone_num;
	}
	public String getShipping_type() {
		return shipping_type;
	}
	public void setShipping_type(String shipping_type) {
		this.shipping_type = shipping_type;
	}
	public String getDefault_type() {
		return default_type;
	}
	public void setDefault_type(String default_type) {
		this.default_type = default_type;
	}
	public String getCheck_type() {
		return check_type;
	}
	public void setCheck_type(String check_type) {
		this.check_type = check_type;
	}
	
}
